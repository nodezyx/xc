// Interactive message response handler (for native flow responses)
cmd({
    on: "message"
}, async (conn, mek, m) => {
    try {
        // Check if this is an interactive message response
        if (mek.message?.interactiveResponseMessage?.nativeFlowResponseMessage) {
            const response = mek.message.interactiveResponseMessage.nativeFlowResponseMessage;
            const params = JSON.parse(response.paramsJson || '{}');
            
            console.log('Interactive response received:', params);
            
            // Handle different response types
            if (params.name === 'single_select' && params.response) {
                const selectedId = params.response.id;
                const from = mek.key.remoteJid;
                
                let responseMessage = '';
                
                switch (selectedId) {
                    case 'ai_feature':
                        responseMessage = 'ü§ñ *AI Feature Selected*\n\nArtificial Intelligence capabilities activated!';
                        break;
                    case 'media_downloader':
                        responseMessage = 'üì• *Media Downloader Selected*\n\nReady to download videos and music!';
                        break;
                    case 'group_tools':
                        responseMessage = 'üë• *Group Tools Selected*\n\nGroup management features enabled!';
                        break;
                    case 'mode_settings':
                        responseMessage = '‚öôÔ∏è *Mode Settings Selected*\n\nUse .mode command to change bot mode!';
                        break;
                    case 'auto_features':
                        responseMessage = 'üîÑ *Auto Features Selected*\n\nConfigure auto-reply with .autoreply command!';
                        break;
                    case 'download_action':
                        responseMessage = 'üì• *Download Action Selected*\n\nUse .video, .audio, or .tiktok commands!';
                        break;
                    case 'settings_action':
                        responseMessage = '‚öôÔ∏è *Settings Action Selected*\n\nUse .setvar to see all settings!';
                        break;
                    case 'info_action':
                        responseMessage = 'üìä *Info Action Selected*\n\nUse .script for bot information!';
                        break;
                    default:
                        responseMessage = 'üîò *Interactive Response Received*\n\nSelected option: ' + selectedId;
                }
                
                await conn.sendMessage(from, { text: responseMessage }, { quoted: mek });
            }
        }
    } catch (error) {
        console.error('Interactive response handler error:', error);
    }
});

