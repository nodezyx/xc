// Helper function to handle image generation
async function handleImageGen(conn, from, mek, m, q, apiUrl, generatorName, isJsonResponse = false, imageUrlKey = 'url') {
    try {
        if (!q) return await conn.sendMessage(from, { text: `âŒ Please provide a prompt for ${generatorName}.\nUsage: .${m.command} [prompt]` }, { quoted: mek });

        // React: Processing
        await conn.sendMessage(from, { react: { text: 'â³', key: m.key } });
        await conn.sendPresenceUpdate("composing", from);

        // Fetch image URL
        const { data } = await axios.get(apiUrl + encodeURIComponent(q));

        let imageUrl;
        if (isJsonResponse) {
            if (!data || (!data[imageUrlKey] && !data.imageUrls)) {
                throw new Error("Invalid API response format");
            }
            imageUrl = data[imageUrlKey] || data.imageUrls[0];
        } else {
            // For direct image responses, use the request URL as image URL
            imageUrl = apiUrl + encodeURIComponent(q);
        }

        if (!imageUrl) {
            throw new Error("No image URL found in response");
        }

        // React: Success
        await conn.sendMessage(from, { react: { text: 'âœ…', key: m.key } });

        // Send generated image
        await conn.sendMessage(from, { 
            image: { url: imageUrl },
            caption: `ðŸ–¼ï¸ *${generatorName}*\n\n` +
                     `ðŸ“ *Prompt:* ${q}\n\n` +
                     `> Â© Generated by Subzero MD`,
            contextInfo: {
                mentionedJid: [m.sender],
                forwardingScore: 999,
                isForwarded: true,
                forwardedNewsletterMessageInfo: {
                    newsletterJid: '120363304325601080@newsletter',
                    newsletterName: 'ã€Ž ð’ð”ðð™ð„ð‘ðŽ ðŒðƒ ã€',
                    serverMessageId: 143
                }
            }
        }, { quoted: mek });

    } catch (e) {
        console.error(`${generatorName} Error:`, e);
        await conn.sendMessage(from, { react: { text: 'âŒ', key: m.key } });
        await conn.sendMessage(from, { text: `âŒ Failed to generate image with ${generatorName}.\nError: ${e.message}` }, { quoted: mek });
    }
}

